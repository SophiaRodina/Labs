import re

print("Родина София, КМ-94. Завдання №1 - Написати програму-фільтр")

n = list(input("Введіть значення з клавіатури: "))
while len(n) != 1:
    print("Ви повинні ввести один символ!")
    n = list(input("Введіть значення з клавіатури: "))
if bool(re.match('^[0-9]{1}$', (n[0]))):
    print("Ви ввели цифру")
else:
    print("Ви ввели букву")